package model.daily;

public class NearTable {

	int table1;
	int table2;
	
	// constructors
	public NearTable(int table1, int table2) {
		this.table1 = table1;
		this.table2 = table2;
	}

	
	//methods
	public int getTable1() {
		return table1;
	}

	public void setTable1(int table1) {
		this.table1 = table1;
	}

	public int getTable2() {
		return table2;
	}

	public void setTable2(int table2) {
		this.table2 = table2;
	}
	
	
}



